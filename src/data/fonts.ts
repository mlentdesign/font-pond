function isBlocklisted(font: Font): boolean {
  // Check slug (strips gf- prefix too)
  const slug = font.slug.replace(/^gf-/, "");
  if (FONT_BLOCKLIST_SLUGS.has(font.id)) return true;
  if (FONT_BLOCKLIST_SLUGS.has(font.slug)) return true;
  if (FONT_BLOCKLIST_SLUGS.has(slug)) return true;
  if (FONT_BLOCKLIST_SLUGS.has(font.id.replace(/^gf-/, ""))) return true;

  // Check name patterns
  const nameLower = font.name.toLowerCase();
  for (const pattern of BLOCKLIST_NAME_PATTERNS) {
    if (nameLower.includes(pattern)) return true;
  }

  return false;
}

