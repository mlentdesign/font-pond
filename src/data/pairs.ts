// ── Merge hand-crafted + dynamic pairs ──

const dynamicPairs = generateDynamicPairs(fonts);

// Deduplicate: hand-crafted pairs take priority over generated ones
const handCraftedSlugs = new Set(handCraftedPairs.map((p) => p.slug));
const uniqueDynamic = dynamicPairs.filter((p) => !handCraftedSlugs.has(p.slug));

export const fontPairs: FontPair[] = [...handCraftedPairs, ...uniqueDynamic];

// ── Lookup helpers ──

export const pairsById = new Map(fontPairs.map((p) => [p.id, p]));
export const pairsBySlug = new Map(fontPairs.map((p) => [p.slug, p]));

export function getPair(idOrSlug: string): FontPair | undefined {
  return pairsById.get(idOrSlug) || pairsBySlug.get(idOrSlug);
}

/**
 * Look up a pair by slug — if not found in the pre-generated set,
 * try to construct it on-the-fly from the slug by finding the header
 * and body fonts. This ensures no pair URL ever 404s.
 */
export function getPairOrConstruct(slug: string): FontPair | null {
  // 1. Try pre-generated lookup
  const existing = pairsBySlug.get(slug);
  if (existing) return existing;

  // 2. Try to parse slug into header + body font slugs
  // Slugs are formatted as "{header-slug}-{body-slug}"
  // Both header and body slugs can contain hyphens, so try all split points
  for (let i = 1; i < slug.length; i++) {
    if (slug[i] !== "-") continue;

    const headerSlug = slug.slice(0, i);
    const bodySlug = slug.slice(i + 1);

    const headerFont = allFontsBySlug.get(headerSlug);
    const bodyFont = allFontsBySlug.get(bodySlug);

    if (headerFont && bodyFont) {
      // Construct the pair dynamically
      const headerTrait = headerFont.distinctiveTraits[0] || headerFont.toneDescriptors[0] || "distinctive character";
      const headerMood = headerFont.toneDescriptors.slice(0, 2).join(", ") || "expressive";
      const allTags = [...new Set([...headerFont.tags.slice(0, 6), ...headerFont.toneDescriptors.slice(0, 4)])].slice(0, 8);

      return {
        id: `constructed-${headerFont.id}-${bodyFont.id}`,
        slug,
        headerFontId: headerFont.id,
        bodyFontId: bodyFont.id,
        rationale: `${headerFont.name}'s ${headerTrait} creates expressive headlines with ${headerMood} energy, while ${bodyFont.name} provides clean, reliable body text. The contrast between the distinctive header and clean body creates strong visual hierarchy.`,
        shortExplanation: `${headerMood} headlines with clean ${bodyFont.name} body text.`,
        toneSummary: `${headerFont.toneDescriptors.slice(0, 3).join(", ")}. Grounded by ${bodyFont.name}'s readability.`,
        useCases: [...headerFont.useCases.slice(0, 3), ...bodyFont.useCases.slice(0, 2)],
        tags: allTags,
        contrastType: `${headerFont.classification}/${bodyFont.classification} contrast`,
        hierarchyStrength: headerFont.isBodySuitable ? 7 : 9,
        bodyLegibilityScore: bodyFont.bodyLegibilityScore || 7,
        practicalityScore: 7,
        originalityScore: 7,
        sourceConfidence: headerFont.source === "other" ? "medium" : "high",
        licenseConfidence: headerFont.licenseConfidence,
        overallScore: 80,
        rankingNotes: `Constructed pair: ${headerFont.name} + ${bodyFont.name}`,
      };
    }
  }

  return null;
}
